    private void changeData(){
        int index = userList.getSelectedIndex();
        User u = (User)listModel.getElementAt(index);
        nameTxt.setText(u.name);
        surnameTxt.setText(u.surname);
        middlenameTxt.setText(u.middlename);
        
    }
    private void update(){
        int index = userList.getSelectedIndex();
        User u = (User)listModel.getElementAt(index);
        if(index != 0){
            c.Update(u.id, nameTxt.getText(), surnameTxt.getText(), middlenameTxt.getText());
        }else{
            return;
        }
    } 
    

