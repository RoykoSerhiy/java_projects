    class LocalListModel extends AbstractListModel{

        private java.util.Map<Integer , User> users = new HashMap<>();
        
        @Override
        public int getSize() {
            return this.users.size();
        }

        @Override
        public Object getElementAt(int index) {
            return users.get(index + 1);
        }
        public void add(User u){
            int oldLast = users.size();
            users.put(u.id, u);
            this.fireIntervalAdded(this, oldLast, users.size() - 1);
            
        }
        public void remove(int index){
            int oldLast = users.size();
            users.remove(index);
            this.fireIntervalRemoved(this, oldLast, users.size());
        }
        
    }
