    public void addButtons()
    {
        JButton btnPlus = new JButton("+");
        JButton btnMinus = new JButton("-");
        JButton btnMult = new JButton("*");
        JButton btnDiv = new JButton("/");
        JButton btnEquals = new JButton("=");
        JButton btnClear = new JButton("C");
        for(int i = 0;i<10;++i){
            JButton btn = new JButton(String.valueOf(i));
            btn.addMouseListener(new ExtendMouseAdapter(i));
            jPanel1.add(btn);
        }
        btnPlus.addMouseListener(new OperationMouseAdapter("+"));
        btnMinus.addMouseListener(new OperationMouseAdapter("-"));
        btnMult.addMouseListener(new OperationMouseAdapter("*"));
        btnDiv.addMouseListener(new OperationMouseAdapter("/"));
        btnEquals.addMouseListener(new OperationMouseAdapter("="));
        btnClear.addMouseListener(new OperationMouseAdapter("C"));
        jPanel1.add(btnPlus);
        jPanel1.add(btnMinus);
        jPanel1.add(btnMult);
        jPanel1.add(btnDiv);
        jPanel1.add(btnEquals);
        jPanel1.add(btnClear);
    }
    
   private void plusBtnClick(){
       if(!"".equals(jTextField1.getText())){
            c.a = Integer.parseInt(jTextField1.getText());
            c.operator = "+";
            jTextField1.setText(jTextField1.getText() + c.operator);
            jTextField2.setText(jTextField1.getText());
            jTextField1.setText("");
        
       }
       else{
           jTextField1.setText("");
       }
        
    }
