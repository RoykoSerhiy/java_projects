    private void equalBtnClick(){
        if(c.a != 0){
            c.b = Integer.parseInt(jTextField1.getText());
            c.calculate();
            jTextField2.setText(jTextField2.getText() + c.b);
            jTextField1.setText("");
            jTextField1.setText(String.valueOf(c.res));
        }else{
            jTextField1.setText(""); 
        }
        
    }
    private void clearBtnClick(){
        c.a = 0;
        c.b = 0;
        c.res = 0;
        jTextField1.setText("");
        jTextField2.setText("");
    }
    private void test(String s){
        
        jTextField1.setText(jTextField1.getText() + s);
    }
    class ExtendMouseAdapter extends MouseAdapter{
        
        final int value;
        
        public ExtendMouseAdapter(int x) {
            this.value = x;
        }
        public int getValue()
        {
            return value;
        }
        @Override
        public void mouseClicked(MouseEvent e) 
        {
            test(String.valueOf(value));
        }
    }
    class OperationMouseAdapter extends MouseAdapter{
        final String value;
        
        public OperationMouseAdapter(String x) {
            this.value = x;
        }
        public String getValue()
        {
            return value;
        }
        @Override
        public void mouseClicked(MouseEvent e) 
        {
            
            switch(value){
                case "+": plusBtnClick();
                    break;
                case "-": minusBtnClick();
                    break;
                case "/": divBtnClick();
                    break;
                case "*": multiBtnClick();
                    break;
                case "C": clearBtnClick();
                    break;
                case "=": equalBtnClick();
                    break;
                default:test(value);
                    break;
            }
        }
    }
